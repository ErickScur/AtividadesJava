package br.edu.ifcvideira.Lista9;

public class Ex6Prof {
	private String nome;
	private String depart;
	private int numReg;
	private int diaAd;
	private int mesAd;
	private int anoAd;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getDepart() {
		return depart;
	}
	public void setDepart(String depart) {
		this.depart = depart;
	}
	public int getNumReg() {
		return numReg;
	}
	public void setNumReg(int numReg) {
		this.numReg = numReg;
	}
	public int getDiaAd() {
		return diaAd;
	}
	public void setDiaAd(int diaAd) {
		this.diaAd = diaAd;
	}
	public int getMesAd() {
		return mesAd;
	}
	public void setMesAd(int mesAd) {
		this.mesAd = mesAd;
	}
	public int getAnoAd() {
		return anoAd;
	}
	public void setAnoAd(int anoAd) {
		this.anoAd = anoAd;
	}
	
}
